package game.bonusGame;

/**
 * A public enum that represents skills that a bonus game Item may possess
 * BUCKETISFULL - indicates that the water bucket is filled with water
 * BUCKETISEMPTY - indicates that the water bucket is empty
 * TICKET - intra-world teleportation ticket
 * FERTILISER -fertiliser for stone tree
 * SWORD - sword, a WeaponItem redeemed from stone tree
 * @author Team Kimchi
 */
public enum BonusGameSkills {
    BUCKETISFULL,BUCKETISEMPTY,TICKET, FERTILISER, SWORD
}
